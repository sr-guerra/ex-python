#Escreva um programa que calcule o tempo de uma viagem de carro.
#Pergunte a distância a precorrer e a velocidade média esperada para a viagem.

distancia = float(input("Qual será a distância percorrida em Km?"))
velocidade_media = float(input("Qual será a velocidade média esperada para a viagem em km/h?"))

tempo = distancia / velocidade_media

print(f"O tempo esperado para a sua viagem é de {tempo} horas.")




